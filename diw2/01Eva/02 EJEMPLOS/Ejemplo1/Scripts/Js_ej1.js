function PonerFoco(elemento) {
    document.getElementById(elemento).focus(); 
}
function CambiarFondo(elemento) {
    document.getElementById(elemento).style.backgroundColor = "#cccccc";
    document.getElementById(elemento).style.fontWeight = "bold";
}
function InicializarFondo(elemento) {
    document.getElementById(elemento).style.backgroundColor = "#10bcef";
    document.getElementById(elemento).style.fontWeight = "normal";
} 